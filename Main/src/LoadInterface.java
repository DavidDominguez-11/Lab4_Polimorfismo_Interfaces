/**
 * Interfaz que define métodos para mostrar recursos y definir días de entrega.
 * @author David Dominguez
 * @version 1.0
 * @since 2023-11-14
 */
public interface LoadInterface {

    /**
     * Muestra la lista de recursos disponibles.
     */
    void showlistResources();
    
    /**
     * Define los días de entrega.
     */
    void returningDays();
}
